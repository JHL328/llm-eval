"""Post-process lm-evaluation-harness output into a standard result.json.

lm_eval writes output like:
    {output_dir}/{model_subdir}/results_{timestamp}.json
    {output_dir}/{model_subdir}/samples_{task}_{timestamp}.jsonl

This script (chained after lm_eval in the SLURM job) finds that results file,
calls the benchmark's parse_result(), and writes our standard result.json.

Usage (generated by SubmitEvaluationUseCase — not called directly):
    python -m llmeval.interfaces.cli.parse_lm_harness \\
        --task ifeval \\
        --model-name ModelName \\
        --model-path /path/to/model \\
        --model-type base \\
        --output-dir /path/to/output_dir
"""

import argparse
import glob
import os
import sys
import traceback
from pathlib import Path


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("--task",       required=True)
    parser.add_argument("--model-name", required=True)
    parser.add_argument("--model-path", required=True)
    parser.add_argument("--model-type", default="base", choices=["base", "sft"])
    parser.add_argument("--output-dir", required=True)
    args = parser.parse_args()

    fail_flag = os.path.join(args.output_dir, "fail.flag")

    try:
        _parse(args)
    except Exception as e:
        try:
            os.makedirs(args.output_dir, exist_ok=True)
            with open(fail_flag, "w") as f:
                f.write(f"{type(e).__name__}: {e}\n\n{traceback.format_exc()}")
        except OSError:
            pass
        print(f"[parse_lm_harness] FAILED: {e}", file=sys.stderr)
        sys.exit(1)


def _parse(args) -> None:
    from llmeval.domain.model import Model, ModelType
    from llmeval.infrastructure.config_loader import ConfigLoader
    from llmeval.infrastructure.result_store import ResultStore
    from llmeval.benchmarks.knowledge.ifeval import IFEvalBenchmark
    from llmeval.benchmarks.likelihood.likelihood_tasks import LikelihoodBenchmark

    repo_root = Path(__file__).resolve().parents[4]
    cfg = ConfigLoader(repo_root)
    benchmark_cfg = cfg.load_benchmark(args.task)

    model = Model(
        name=args.model_name,
        path=args.model_path,
        type=ModelType(args.model_type),
    )

    # lm_eval creates: {output_dir}/{model_subdir}/results_{timestamp}.json
    # Find the first subdirectory (same pattern as evaluate_likelihood.py)
    subdirs = sorted([
        d for d in Path(args.output_dir).iterdir() if d.is_dir()
    ])
    result_json_path = None
    for subdir in subdirs:
        matches = sorted(subdir.glob("results_*.json"))
        if matches:
            result_json_path = str(matches[-1])
            break

    if result_json_path is None:
        raise FileNotFoundError(
            f"No results_*.json found under {args.output_dir}. "
            "Did lm_eval run successfully?"
        )
    print(f"[parse_lm_harness] Parsing {result_json_path}")

    if args.task == "ifeval":
        benchmark = IFEvalBenchmark(benchmark_cfg, repo_root)
    else:
        benchmark = LikelihoodBenchmark(benchmark_cfg, repo_root)

    result = benchmark.parse_result(result_json_path, model)
    if result is None:
        raise ValueError(
            f"parse_result returned None for task '{args.task}'. "
            f"Check metric key mapping in likelihood_tasks.py."
        )

    store_root = str(Path(args.output_dir).parents[1])
    store = ResultStore(store_root)
    store.write_result(result, args.task)
    print(f"[parse_lm_harness] Done. metrics: {result.metrics}")


if __name__ == "__main__":
    main()
