"""GPU-side evaluation runner — invoked by each SLURM job script.

Usage (generated by SubmitEvaluationUseCase, not called directly by users):

    python -m llmeval.interfaces.cli.run_job \\
        --task gsm8k \\
        --model-path /path/to/checkpoint \\
        --model-name ModelName \\
        --model-type base \\
        --gpus 1 \\
        --output-dir /path/to/output_dir
"""

import argparse
import os
import sys
import traceback
from pathlib import Path

# Mapping: task name → (module, class_name)
_REGISTRY = {
    "gsm8k":     ("llmeval.benchmarks.math.gsm8k",         "GSM8KBenchmark"),
    "math500":   ("llmeval.benchmarks.math.math500",        "Math500Benchmark"),
    "mmlu":      ("llmeval.benchmarks.knowledge.mmlu",      "MMLUBenchmark"),
    "mmlu_pro":  ("llmeval.benchmarks.knowledge.mmlu_pro",  "MMLUProBenchmark"),
    "mmlu_flan": ("llmeval.benchmarks.knowledge.mmlu_flan", "MMLUFLANBenchmark"),
    "bbh":       ("llmeval.benchmarks.knowledge.bbh",       "BBHBenchmark"),
    "gpqa":      ("llmeval.benchmarks.knowledge.gpqa",      "GPQABenchmark"),
    "humaneval": ("llmeval.benchmarks.code.humaneval",      "HumanEvalBenchmark"),
    "mbpp":      ("llmeval.benchmarks.code.mbpp",           "MBPPBenchmark"),
}


def _load_benchmark_class(task_name: str):
    if task_name not in _REGISTRY:
        raise ValueError(
            f"Unknown task '{task_name}'. vLLM tasks: {', '.join(_REGISTRY)}"
        )
    module_path, class_name = _REGISTRY[task_name]
    import importlib
    module = importlib.import_module(module_path)
    return getattr(module, class_name)


def main() -> None:
    parser = argparse.ArgumentParser(description="Run a single benchmark evaluation job.")
    parser.add_argument("--task",        required=True)
    parser.add_argument("--model-path",  required=True)
    parser.add_argument("--model-name",  required=True)
    parser.add_argument("--model-type",  default="base", choices=["base", "sft"])
    parser.add_argument("--gpus",        type=int, default=1)
    parser.add_argument("--output-dir",  required=True)
    args = parser.parse_args()

    os.makedirs(args.output_dir, exist_ok=True)
    fail_flag = os.path.join(args.output_dir, "fail.flag")

    try:
        _run(args)
    except Exception as e:
        with open(fail_flag, "w") as f:
            f.write(f"{type(e).__name__}: {e}\n\n{traceback.format_exc()}")
        print(f"[run_job] FAILED: {e}", file=sys.stderr)
        sys.exit(1)


def _run(args) -> None:
    from llmeval.domain.model import Model, ModelType
    from llmeval.infrastructure.config_loader import ConfigLoader
    from llmeval.infrastructure.result_store import ResultStore
    from llmeval.infrastructure.vllm.inference_runner import VLLMRunner

    # run_job.py lives at src/llmeval/interfaces/cli/run_job.py → repo root is 4 levels up
    repo_root = Path(__file__).resolve().parents[4]

    cfg = ConfigLoader(repo_root)
    benchmark_cfg = cfg.load_benchmark(args.task)

    model = Model(
        name=args.model_name,
        path=args.model_path,
        type=ModelType(args.model_type),
        gpus=args.gpus,
    )

    BenchmarkClass = _load_benchmark_class(args.task)
    benchmark = BenchmarkClass(benchmark_cfg, repo_root)

    print(f"[run_job] Loading dataset for {args.task}...")
    examples = benchmark.load_dataset()
    prompts  = [benchmark.build_prompt(ex) for ex in examples]
    print(f"[run_job] {len(prompts)} examples loaded.")

    # Build sampling config, patching in task-specific stop tokens
    sampling_cfg = benchmark_cfg.sampling_config
    stop = benchmark.stop_tokens
    if stop and stop != list(sampling_cfg.stop):
        from llmeval.domain.sampling_config import SamplingConfig
        sampling_cfg = SamplingConfig(
            temperature=sampling_cfg.temperature,
            top_p=sampling_cfg.top_p,
            n_sampling=sampling_cfg.n_sampling,
            max_tokens=sampling_cfg.max_tokens,
            stop=stop,
            seed=sampling_cfg.seed,
            k_list=sampling_cfg.k_list,
        )

    print(
        f"[run_job] Inference: {sampling_cfg.n_sampling} samples × "
        f"{len(prompts)} problems (temp={sampling_cfg.temperature})"
    )
    runner = VLLMRunner.from_model(model)
    predictions = runner.generate(prompts, sampling_cfg)

    print("[run_job] Evaluating predictions...")
    result = benchmark.build_result(model=model, predictions=predictions, examples=examples)

    # Write result.json via ResultStore (handles atomic write)
    # output_dir is  <output_root>/<task>/<model_name>  — store root is two levels up
    store_root = str(Path(args.output_dir).parents[1])
    store = ResultStore(store_root)
    store.write_result(result, args.task)

    print(f"[run_job] Done. accuracy={result.accuracy:.4f}  n={result.n_total}")
    print(f"[run_job] metrics: {result.metrics}")


if __name__ == "__main__":
    main()
